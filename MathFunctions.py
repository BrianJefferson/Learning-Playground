import math as m

#ceil(), floor(), trunc(), pi()

print(m.ceil(3.6))
print(m.floor(3.6))
print(m.trunc(3.6))
print(m.pi)


# factorial()
# 3! = 3 * 2 * 1 = 6
# 4! = 4 * 3 * 2 * 1 = 24

print(m.factorial(3))
print(m.factorial(4))

# sqrt()
print(m.sqrt(16))
print(m.sqrt(100))

# hypot()

print(m.hypot(3, 4))
print(m.hypot(6, 12))
